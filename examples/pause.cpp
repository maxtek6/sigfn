#include <sigfn.hpp>
#include <iostream>

int main(int argc, const char **argv)
{
    // notify user that the program is "paused"
    std::cout << "Paused. Press Ctrl+C to exit." << std::endl;
    
    sigfn::wait({SIGINT});

    std::cout << std::endl << "Received signal: SIGINT" << std::endl;

    return 0;
}